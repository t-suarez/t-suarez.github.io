// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

//I: 2 numbers
//O: smallest value parameter
//C: none
//E: none
function min(num1, num2) {
  if(num1 < num2) return num1;
  return num2;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

//I: number
//O: boolean value representing true if the number is even and false if odd
//C: must be a number
//E: none
function isEven(num) {
  
  var normalized = (num < 0) ? -num : num; //if num is negative, negate it
  
  if(normalized === 0){
    return true;
  }
  if(normalized === 1){
    return false;
  }
  else{
    return isEven(normalized - 2);
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

//I: char to be counted, string to search
//O: number of instances of char in string
//C: must be string
//E: none
function countChars(string, char) {
  var counter = 0;
  for(let i = 0; i < string.length; i++)
    if(string[i] === char){
      counter++;
    }
  return counter;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

//I: string to search
//O: number of instances of "B" in string
//C: must be a string
//E: none
function countBs(string) {
  return countChars(string, "B");
}

